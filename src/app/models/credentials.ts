/**
 * The Credentials Object of the user
 *
 * @export
 * @interface Credentials
 */
export interface Credentials {

    /**
     * the email
     *
     * @type {String}
     * @memberOf Credentials
     */
    email: string;

    /**
     * the password
     *
     * @type {String}
     * @memberOf Credentials
     */
    password: string;
}
