/**
 * AcceptApplication
 *
 * @export
 * @interface AcceptApplication
 */
export interface AcceptApplication {

    /**
     * Accept Message
     *
     * @type {String}
     * @memberOf AcceptApplication
     */
    accept_message: string;

    /**
     * Flag to indicate if the Application requires changes
     *
     * @type {Boolean}
     * @memberOf AcceptApplication
     */
    accept_requiresChanges: boolean;
}
